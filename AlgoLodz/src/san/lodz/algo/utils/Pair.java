package san.lodz.algo.utils;

public class Pair {
    int a;
    int b;

    Pair(int a, int b)
    {
        this.a = a;
        this.b = b;
    }

    @Override
    public String toString() {
        return "Pair{" +
                "a=" + a +
                ", b=" + b +
                '}';
    }
}
